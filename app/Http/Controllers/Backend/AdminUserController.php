    {
      
        $request->validate([
          'name' => 'required|max:100',
          'email' => 'required|email|max:200',
          'password' => 'required|min:6',
          'password_confirmation' => 'required|min:6|confirmed',
          'avatar' => 'image|size:max:2048',
          'role' => 'required'
        ]);
       
    }

